# a dictionary representing a user
alice = {'name': 'Alice', 'age': 32, 'city': 'Madrid'}
print(alice)

# another dictionary for another user
bob = dict(name='Bob', age=42, city='Berlin')
print(bob)

# a dictionary representing more users
users = {1: alice, 2: bob}
print(users)
