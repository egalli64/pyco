# a string delimited by single quotes
s1 = 'a string'
print(type(s1), s1)

# a string delimited by double quotes
s2 = "another string"
print(type(s2), s2)

# a string delimited by triple quotes
s3 = """a multiline string
is useful to represent a long text
that should be read by humans"""
print(type(s3), s3)
