# A string
s = "Welcome to Python World"
print("The original string:", s)


print("Replacing 'Python' with 'Programming':", s.replace("Python", "Programming"))
print("Replacing 'o' with '_':", s.replace("o", "_"))
print("Replacing 'o' with '' (remove):", s.replace("o", ""))
