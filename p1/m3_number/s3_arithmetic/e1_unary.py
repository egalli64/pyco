# on int
x = 4
print("x is", x)

y = +x
print(" if y = +x, its value is", y)  # 4
y = -x
print(" if y = -x, its value is", y)  # -4

# on float
x = 4.2
print("x is", x)

y = +x
print(" if y = +x, its value is", y)  # 4.2
y = -x
print(" if y = -x, its value is", y)  # -4.2

# on bool
x = True
print("x is", x)

y = +x
print(" if y = +x, its value is", y)  # 1
y = -x
print(" if y = -x, its value is", y)  # -1
