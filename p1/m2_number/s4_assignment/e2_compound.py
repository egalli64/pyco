x = 2
print("x is", x)

x += 8
print("After 'x += 8', x is", x)  # 10

x -= 3
print("After 'x -= 3', x is", x)  # 7

x *= 2
print("After 'x *= 2', x is", x)  # 14

x **= 2
print("After 'x **= 2', x is", x)  # 196

x /= 5
print("After 'x /= 5', x is", x)  # 39.2

x //= 7
print("After 'x /= 7', x is", x)  # 5.0

x %= 5
print("After 'x %= 5', x is", x)  # 0.0
