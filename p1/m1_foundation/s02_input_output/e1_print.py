# print a single string to the console
print("Hello, Python")

# print more arguments
print("Hello", "Python")

# use an underscore as separator
print("Hello", "Python", sep="_")

# do not add a newline at the end of each print
print("Hello", end="")
print("Python", end="")

# print just a newline
print()
