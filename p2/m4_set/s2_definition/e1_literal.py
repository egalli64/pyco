# a literal set
friends = {"bob", "tom", "kim"}
print("A literal set:", friends)

# Oh, no! this is an empty _dictionary_!
dict = {}
print("This is _not_ a set:", dict, type(dict))

# an empty set by constructor
enemies = set()
print("An empty set by constructor:", enemies, type(enemies))
